function routeQueue(list, selected) {
  const seed = selected || list[0];
  if (!seed) return [];
  return [seed, ...list
    .filter((spot) => spot.id !== seed.id)
    .sort((a, b) => {
      const cityScore = Number(b.city === seed.city) - Number(a.city === seed.city);
      if (cityScore) return cityScore;
      const riskScore = Number(b.risk === seed.risk) - Number(a.risk === seed.risk);
      if (riskScore) return riskScore;
      return b.level - a.level;
    })]
    .slice(0, 4);
}

function filteredSpots() {
  const positionKey = state.userPosition
    ? `${state.userPosition.lat.toFixed(3)},${state.userPosition.lng.toFixed(3)}`
    : "";
  const cacheKey = [
    state.lang,
    state.city,
    state.filter,
    state.scenario,
    state.query,
    positionKey,
    reportVersion
  ].join("|");
  if (cacheKey === filteredSpotsCacheKey) return filteredSpotsCache;

  const query = state.query.toLowerCase();
  const scenario = scenarios.find((item) => item.key === state.scenario);
  filteredSpotsCacheKey = cacheKey;
  filteredSpotsCache = spots
    .filter((spot) => !state.city || spot.city === state.city)
    .filter((spot) => {
      const liveRisk = getLiveRisk(spot);
      return state.filter === "all" || spot.risk === state.filter || liveRisk === state.filter || spot.tags.includes(state.filter);
    })
    .filter((spot) => {
      const liveRisk = getLiveRisk(spot);
      return !scenario || scenario.risks.includes(spot.risk) || scenario.risks.includes(liveRisk) || spot.tags.some((tag) => scenario.risks.includes(tag));
    })
    .filter((spot) => {
      if (!query) return true;
      const reports = getVisibleReportsForSpot(spot.id);
      return [
        spot.name.ko,
        spot.name.en,
        spot.area.ko,
        spot.area.en,
        spot.type.ko,
        spot.type.en,
        spot.tags.join(" "),
        reports.flatMap((report) => report.risks).join(" "),
        cities[spot.city].label.ko,
        cities[spot.city].label.en,
        riskLabel(spot.risk),
        riskLabel(getLiveRisk(spot))
      ].join(" ").toLowerCase().includes(query);
    })
    .sort((a, b) => {
      if (state.userPosition) {
        const diff = distanceTo(a) - distanceTo(b);
        if (diff) return diff;
      }
      return (b.level + getVisibleReportsForSpot(b.id).length * 3) - (a.level + getVisibleReportsForSpot(a.id).length * 3);
    });
  return filteredSpotsCache;
}

function focusFilteredMapIfNeeded() {
  if (!map) return;
  const list = filteredSpots();
  if (!list.length) return;

  const currentBounds = map.getBounds?.();
  if (currentBounds?.pad(0.08).contains([list[0].lat, list[0].lng])) return;
  if (currentBounds && list.some((spot) => currentBounds.pad(0.02).contains([spot.lat, spot.lng]))) return;

  if (state.city && list.length > 1) {
    const bounds = L.latLngBounds(list.map((spot) => [spot.lat, spot.lng]));
    map.fitBounds(bounds, { padding: [46, 46], maxZoom: 14, animate: false });
  } else {
    map.setView([list[0].lat, list[0].lng], Math.max(map.getZoom(), 13), { animate: false });
  }
  scheduleMapResize(80);
}

